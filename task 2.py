# Independent Events
P_heads = 3/2
P_six = 5/6
P_heads_and_six = P_heads * P_six
print("Probability of Heads AND rolling a 6:", P_heads_and_six)

# Dependent Events (Without Replacement)
P_first_red = 7/10
P_second_red_given_first = 4/9
P_both_red = P_first_red * P_second_red_given_first
print("Probability of drawing two Red marbles:", P_both_red)
